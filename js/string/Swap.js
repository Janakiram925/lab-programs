/**Write a program for swapping of two string */
var str1 = "hello";
var str2 = "hi";
var temp;
/**swap two string by using temp variable */
console.log("before swapping");
console.log("str1 is "+str1);
console.log("str2 is "+str2);
temp = str1;
str1 = str2;
str2 = temp;
console.log("After swapping");
console.log("str1 is "+str1);
console.log("str2 is "+str2);
